public class operators {
    
    public static void main(String[] args) {
        //operators
        double a = 1;
        double b = 2;
        double sum = a + b;
        double diff = a - b;
        double mul = a * b;
        double div = a / b;
        double mod = a % b;
        System.out.println(sum);
        System.out.println(div);
        System.out.println(mul);
        System.out.println(diff);
        System.out.println(mod);

    }
}
