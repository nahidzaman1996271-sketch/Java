public class while_loop {
    
    public static void main(String[] args) {

        int i = 1;
        while(i <= 100){
            System.out.println(i);
            i++;
        }
        System.out.println("Now we are gonna print them in reverse order: ");
        int j = 100;
        while(j >= 1){
            System.out.println(j);
            j--;
        }
    }
}
