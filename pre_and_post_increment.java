public class pre_and_post_increment{

    public static void main(String[] args){
    //pre and post increment

    int numb = 1;
    System.out.println(++numb); //this is the preincrement part
    System.out.println(numb);  //and this is after the preincrement
    System.out.println(numb--); //this will decrement one value to the next steps
    System.out.println(numb); //and here the value will be decrements and will become 1
    }
}